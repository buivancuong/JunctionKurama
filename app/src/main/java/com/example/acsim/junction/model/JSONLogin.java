package com.example.acsim.junction.model;

public class JSONLogin {
    private String status;
    private String user;

    public JSONLogin() {
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }
}
